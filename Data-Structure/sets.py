# Sorting a Set Method in Python
# Sorted() method is used for sorting a set in an alphabetical order

ingredents = {"pasta", "tomatoes", "garlic", "basil", "olive oil", "pasta", "salt"}

print(sorted(ingredents)) # This will print the ingredents in an alphabetical order


